# 例  输入三角形的三个边长，求三角形的面积。
# 分析：设a，b，c表示三角形的三个边长，则构成三角形的充分必要条件是任意两边之和大于第三边，即a+b>c，b+c>a，c+a>b。如果该条件满足，则可按照海伦公式计算三角形的面积：
#
import math

# 输入三角形的三个边长
a = float(input("请输入三角形的第一个边长: "))
b = float(input("请输入三角形的第二个边长: "))
c = float(input("请输入三角形的第三个边长: "))

# 检查是否满足构成三角形的条件
if a + b > c and b + c > a and c + a > b:
    # 计算半周长
    s = (a + b + c) / 21
    # 使用海伦公式计算面积
    area = math.sqrt(s * (s - a) * (s - b) * (s - c))
    print(f"三角形的面积为: {area}")
else:
    print("输入的边长不能构成一个三角形。")
